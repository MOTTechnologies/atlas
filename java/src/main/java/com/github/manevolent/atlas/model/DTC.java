    public String getName() {
        int code = getCode() & 0xFFFF;
        StringBuilder codeString = new StringBuilder(Integer.toHexString(code));
        while (codeString.length() < 4) {
            codeString.insert(0, "0");
        }
        return (system != null ? system.getCode() : "") + codeString;
    }

